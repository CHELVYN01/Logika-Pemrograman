#Buatlah program untuk menggabungkan dua array menjadi satu array baru.



# Contoh dua array bilangan bulat
array1 = [1, 2, 3, 4, 5]
array2 = [6, 7, 8, 9, 10]

# Menggabungkan kedua array
array_gabungan = array1 + array2

# Menampilkan hasil
print("Array pertama:", array1)
print("Array kedua:", array2)
print("Array gabungan:", array_gabungan)
