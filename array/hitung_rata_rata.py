# Contoh array bilangan bulat
array_bilangan = [10, 20, 30, 40, 50]

# Menghitung rata-rata
jumlah_elemen = len(array_bilangan)
jumlah_total = sum(array_bilangan)
rata_rata = jumlah_total / jumlah_elemen

# Menampilkan hasil
print("Array bilangan:", array_bilangan)
print("Rata-rata nilai:", rata_rata)
