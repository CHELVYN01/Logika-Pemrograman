#bilangan genap atau ganjil.



bilangan = int(input("masukan nilai bilangn :"))

# Menentukan genap atau ganjil
if bilangan % 2 == 0 :
    print(f"{bilangan} adalah bilangan genap.")
else:
    print(f"{bilangan} adalah bilangan ganjil.")




